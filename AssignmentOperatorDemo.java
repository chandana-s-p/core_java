class AssignmentOperatorDemo{
public static void main(String[] args){
   int m=50;
   int n=10;
   n+=m;
System.out.println(" n : "+n);
   int i=50;
   int j=10;
   i/=j;
System.out.println(" i : "+i);
   int a=50;
   int b=10;
   a-=b;
System.out.println(" a : "+a);
   int x=50;
   int y=10;
   x*=y;
System.out.println(" x : "+x);
}
}